-- ============================================================
-- 023: firm_profiles 加欄位 ex_judicial_officers
-- ============================================================
-- 目的：記錄事務所中「前司法官」（前法官、前檢察官、前特偵組等）律師。
-- 這是台灣法律事務所的重要差異化因子 — 前司法官背景的律師通常
-- 能帶來實務公信力、法庭人脈、判決傾向洞察等競爭優勢。
--
-- 格式：TEXT[]，每筆為「姓名｜職稱｜附註」格式，例如：
-- - "陳樹村｜前高雄地方法院法官｜23 年法官資歷"
-- - "黃國益｜前台灣高等法院法官｜創辦人"
-- - "陳守煌｜前檢察總長｜2016 創所"
-- ============================================================

ALTER TABLE firm_profiles
ADD COLUMN IF NOT EXISTS ex_judicial_officers TEXT[];

COMMENT ON COLUMN firm_profiles.ex_judicial_officers IS
'此事務所中具「前司法官」身分的律師列表（前法官、前檢察官等）。格式：姓名｜職稱｜附註';
